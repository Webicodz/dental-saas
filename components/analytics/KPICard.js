const formatValue = (value, format) => {
  switch (format) {
    case 'currency':
      return new Intl.NumberFormat('en-US', {
        style: 'currency',
        currency: 'USD',
        minimumFractionDigits: 0,
        maximumFractionDigits: 0,
      }).format(value);
    case 'percentage':
      return `${value}%`;
    default:
      return new Intl.NumberFormat('en-US').format(value);
  }
};

export default function KPICard({ title, value, trend, icon, format, subtitle }) {
  const isPositiveTrend = trend > 0;
  const isNeutralTrend = trend === 0;

  const iconElement = icons[icon] || icons.chart;

  return (
    <div className="bg-white rounded-xl shadow-sm border border-gray-200 p-6 hover:shadow-md transition-shadow">
      <div className="flex items-start justify-between">
        <div className="flex-1">
          <p className="text-sm font-medium text-gray-600 mb-1">{title}</p>
          <p className="text-2xl font-bold text-gray-900">
            {formatValue(value, format)}
          </p>
          {subtitle && (
            <p className="text-sm text-gray-500 mt-1">{subtitle}</p>
          )}
        </div>
        <div className={`p-3 rounded-lg ${
          icon === 'dollar' || icon === 'percentage' ? 'bg-green-100 text-green-600' :
          icon === 'x' ? 'bg-red-100 text-red-600' :
          icon === 'check' ? 'bg-emerald-100 text-emerald-600' :
          'bg-blue-100 text-blue-600'
        }`}>
          {iconElement}
        </div>
      </div>
      
      {trend !== undefined && (
        <div className="mt-4 flex items-center">
          {!isNeutralTrend ? (
            <>
              <span className={`inline-flex items-center text-sm font-medium ${
                isPositiveTrend ? 'text-green-600' : 'text-red-600'
              }`}>
                {isPositiveTrend ? (
                  <svg className="w-4 h-4 mr-1" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 10l7-7m0 0l7 7m-7-7v18" />
                  </svg>
                ) : (
                  <svg className="w-4 h-4 mr-1" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 14l-7 7m0 0l-7-7m7 7V3" />
                  </svg>
                )}
                {Math.abs(trend)}%
              </span>
              <span className="text-sm text-gray-500 ml-2">vs last period</span>
            </>
          ) : (
            <span className="text-sm text-gray-500">No change</span>
          )}
        </div>
      )}
    </div>
  );
}
